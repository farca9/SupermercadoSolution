package search.util;

public class MapUnitUpdater {

	private MapUnit mapUnit;
	private int x;
	private int y;
	
	
	public MapUnit getMapUnit() {
		return mapUnit;
	}
	public void setMapUnit(MapUnit mapUnit) {
		this.mapUnit = mapUnit;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
